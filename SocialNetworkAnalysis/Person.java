
public class Person {

	private int id;
	private int contact;
	
	public Person(int id, int contact) {
		this.id = id;
		this.contact = contact;
	}
	
	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public int getContact() {
		return contact;
	}

	public void setContact(int contact) {
		this.contact = contact;
	}
}
